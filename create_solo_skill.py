        tables = len(re.findall(r'\|.*\|.*\|', content))
        table_score = min(10, tables * 0.3)
        tests.append(("表格丰富度", table_score, 10))
        
        # 4. 代码示例
        codes = len(re.findall(r'```[\w]*\n', content))
        code_score = min(10, codes * 2)
        tests.append(("代码示例", code_score, 10))
        
        # 5. 案例
        cases = content.count('案例')
        case_score = min(15, cases * 3)
        tests.append(("案例丰富度", case_score, 15))
        
        # 6. 可操作性
        action_score = 0
        if '检查清单' in content: action_score += 5
        if '三步' in content: action_score += 5
        if '公式' in content: action_score += 5
        tests.append(("可操作性", action_score, 15))
        
        # 7. 完整性
        complete_score = 0
        if len(content) > 8000: complete_score += 5
        if '更新日志' in content: complete_score += 5
        if '核心认知' in content: complete_score += 5
        tests.append(("完整性", complete_score, 15))
        
