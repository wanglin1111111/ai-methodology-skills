        yaml_score = 0
        required = ['name', 'version', 'description', 'display_name', 'stability']
        for field in required:
            if re.search(f'^{field}:', content, re.MULTILINE):
                yaml_score += 3
        tests.append(("YAML Frontmatter", yaml_score, 15))
        
        # 2. 核心章节
        sections = [
            '获客三步法',
            '工具栈',
            '内容营销',
            '合作获客',
            '转型路径',
            '实战案例'
        ]
