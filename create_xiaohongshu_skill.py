    def test_skill(self, skill_file):
        """测试技能"""
        print("\n" + "=" * 60)
        print("技能质量测试")
        print("=" * 60)
        
        with open(skill_file, 'r', encoding='utf-8') as f:
            content = f.read()
        
        tests = []
        
        # 1. YAML Frontmatter
        yaml_score = 0
        required = ['name', 'version', 'description', 'display_name', 'stability']
        for field in required:
            if re.search(f'^{field}:', content, re.MULTILINE):
                yaml_score += 3
        tests.append(("YAML Frontmatter", yaml_score, 15))
        
        # 2. 核心章节
        sections = [
            'IP定位',
            '爆款选题',
            '封面设计',
            '文案结构',
            'SEO',
            '引流',
            '转化'
        ]
        section_score = sum(1 for s in sections if s in content) * 2.5
        tests.append(("核心章节", section_score, 17))
        
        # 3. 表格
        tables = len(re.findall(r'\|.*\|.*\|', content))
        table_score = min(10, tables * 0.3)
        tests.append(("表格丰富度", table_score, 10))
        
        # 4. 代码示例
        codes = len(re.findall(r'```[\w]*\n', content))
        code_score = min(10, codes * 2)
        tests.append(("代码示例", code_score, 10))
        
        # 5. 案例
        cases = content.count('案例')
        case_score = min(15, cases * 3)
        tests.append(("案例丰富度", case_score, 15))
        
        # 6. 可操作性
        action_score = 0
        if '检查清单' in content: action_score += 5
        if '公式' in content: action_score += 5
        if '模板' in content: action_score += 5
        tests.append(("可操作性", action_score, 15))
        
        # 7. 完整性
        complete_score = 0
        if len(content) > 8000: complete_score += 5
        if '更新日志' in content: complete_score += 5
        if '核心认知' in content: complete_score += 5
        tests.append(("完整性", complete_score, 15))
        
        total = 0
        for name, got, max_val in tests:
            print(f"  {name}: {got:.1f}/{max_val}")
            total += got
        
        print(f"\n总分: {total:.1f}/100")
        
        if total >= 90:
            print("评级: ⭐⭐⭐⭐⭐ 优秀")
        elif total >= 80:
            print("评级: ⭐⭐⭐⭐ 良好")
        else:
            print("评级: ⭐⭐⭐ 一般")
        
        return total
        
